package com.example.lenovo.hire_me;

public class Questions {
    public String date,name,ques,time,uid;

    public Questions(){

    }
    public Questions(String date, String name, String ques, String time, String uid) {
        this.date = date;
        this.name = name;
        this.ques = ques;
        this.time = time;
        this.uid = uid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getQues() {
        return ques;
    }

    public void setQues(String ques) {
        this.ques = ques;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
